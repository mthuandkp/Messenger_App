    private void performEvent() {
        ActionListener ac = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                String user = username.getText();
                String pass = String.valueOf(password.getPassword());
                                
                if(user.compareTo("") == 0){
                    JOptionPane.showMessageDialog(null, "Please input username");
                    return;
                }
                else if(pass.compareTo("") == 0){
                    JOptionPane.showMessageDialog(null, "Please input password");
                    return;
                }
                
                Object []obj = new Object[2];
                obj[0] = user;
                obj[1] = pass;
                
                try {
                    s = new Socket("127.0.0.1",8888);
                    DataInputStream input = new DataInputStream(s.getInputStream());
                    DataOutputStream output = new DataOutputStream(s.getOutputStream());
                    
                    
                    HashMap<String,String> maps = new HashMap<>();
                    
                    maps.put("command", "CHECK_LOGIN");
                    maps.put("data",new Gson().toJson(obj));
                    
                    output.writeUTF(new Gson().toJson(maps));
                    
                    
                    
                    
                    //Read result from server
                    String line = input.readUTF();System.out.println(line);
                    maps = new Gson().fromJson(line, new TypeToken<HashMap<String,String>>(){}.getType());
                    
                    if(maps.get("result").compareTo("SUCCESS") == 0){
                        User userAccount = new Gson().fromJson(maps.get("data"), new TypeToken<User>(){}.getType());
                        new Chat_Room(userAccount).setVisible(true);
                        s.close();
                        dispose();
                    }
                    else{
                        JOptionPane.showMessageDialog(null, maps.get("result"));
                    }
                    
                    
                } catch (IOException ex) {
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        };
        
        login.addActionListener(ac);
    }
